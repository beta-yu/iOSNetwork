#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.weatherInfo.titleNames.count;
}

- (nonnull UITableViewCell *)tableView:(nonnull UITableView *)tableView cellForRowAtIndexPath:(nonnull NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"reuseIdentifier"];
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:@"reuseIdentifier"];
    }
    cell.textLabel.text = self.weatherInfo.titleNames[indexPath.row];
    switch (indexPath.row) {
        case 0:
            cell.detailTextLabel.text = self.weatherInfo.info;
            break;
        case 1:
            cell.detailTextLabel.text = self.weatherInfo.temperature;
            break;
        case 2:
            cell.detailTextLabel.text = self.weatherInfo.power;
            break;
        case 3:
            cell.detailTextLabel.text = self.weatherInfo.direct;
            break;
        case 4:
            cell.detailTextLabel.text = self.weatherInfo.aqi;
            break;
        default:
            cell.detailTextLabel.text = nil;
            break;
    }
    return cell;
}

